
//OPERADORES ARITMÉTICOS
let a = 5;
let b = 7;

console.log(a + b, 'adición');
console.log(a - b, 'resta');
console.log(a / b, 'división');
console.log(a * b, 'multiplicación');
console.log(a % b, 'modulo');
console.log(a ** b, 'potencia');


//INCREMENTO
console.log(a++); 
console.log(++a);

//DECREMENTO
console.log(a--); 
console.log(--a);

//OPERADORES DE ASIGNACIÓN
 a += 5;
 a -= 5;
 a *= 5;
 a /= 5;
 a %= 5;
 a **= 5;

 //OPERADORES DE COMPARACIÓN
 console.log(a > 5);
 console.log(a >= 5);
 console.log(a < 5);
 console.log(a <= 5);

 console.log(a == 5);
 console.log(a != 5);
 console.log(a === 5);
 console.log(a !== 5);

